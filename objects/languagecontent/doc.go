/*
Package languagecontent implements the STIX 2.1 Language Content object.

The following information comes directly from the STIX 2.1 specification.

This implements STIX 2.1 specification section 7.2 - Language Content Object.

The Language Content object represents text content for STIX Objects represented
in languages other than that of the original object. Language content may be a
translation of the original object by a third-party, a first-source translation
by the original publisher, or additional official language content provided at
the time of creation.
*/
package languagecontent
